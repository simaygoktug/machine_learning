# Örnek: Bir fatura yönetim sistemi geliştirerek tek sorumluluk ve kapsülleme uygulayın.

# Recursion (Özyineleme)
# Fibonacci, Factorial gibi algoritmaları özyineleme ile yazın. Örnek: Bir dosya sisteminde tüm dosyaları listeleyen bir fonksiyon geliştirin.

# Polymorphism (Çok Biçimlilik)
# Aynı işlevin farklı sınıflarda nasıl farklı davranış gösterebileceğini anlayın.
# Örnek: Kedi ve köpek için ayrı speak() metotları tanımlayan bir hayvan sınıflandırma sistemi yazın.

# Model Dağıtımı ve İzleme
# Flask ile bir tahmin modeli API'si oluşturun, Docker ile dağıtın ve Prometheus ile izleyin.
# Örnek: Flask üzerinden tahmin servisi oluşturup metrikleri Prometheus ve Grafana ile görselleştirin.

# Veri Mühendisliği ve Görselleştirme
# Pandas ile eksik verileri doldurun ve veri analizi yapın. Seaborn kullanarak kategorik veriler için sütun grafikleri oluşturun.
# Örnek: Bir e-ticaret satış veri setinde ürün kategorilerine göre satış analizi yapın.

# Model Optimizasyonu ve Hiperparametre Ayarı
# Grid Search ve Random Search ile hiperparametre optimizasyonu yapmayı öğrenin.
# Örnek: SVM modelini Iris veri seti üzerinde optimize edin.

# Zaman Serisi Analizi
# ARIMA veya Prophet kullanarak zaman serisi tahminleri yapın.
# Örnek: Aylık satış verisini kullanarak ARIMA ile gelecekteki 6 ayın satışlarını tahmin edin.

# Doğal Dil İşleme (NLP)
# Hugging Face Transformers ve BERT kullanarak metin sınıflandırma veya duygu analizi yapın.
# Örnek: Küçük bir veri setiyle duygu analizi modeli geliştirin ve sonuçları görselleştirin.

# Makine Öğrenmesi Algoritmalarını Anlama
# KNN veya Logistic Regression gibi algoritmaları sıfırdan kodlayarak temel prensipleri öğrenin.
# Örnek: KNN algoritmasını sıfırdan uygulayarak bir veri sınıflandırma problemi çözün.

